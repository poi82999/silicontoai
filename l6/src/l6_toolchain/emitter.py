def _emit_tiled_tile(
    tile_dir: Path,
    tile_id: int,
    spec: LinearOp,
    tile: TilePlanEntry,
    activations: np.ndarray,
    weights: np.ndarray,
    product: np.ndarray,
) -> dict[str, object]:
    m_base = tile.m_tile_base
    n_base = tile.n_tile_base
    k_base = tile.k_tile_base
    tile_m = tile.tile_m
    tile_k = tile.tile_k
    tile_n = tile.tile_n

    activation_slice = activations[m_base : m_base + tile_m, k_base : k_base + tile_k]
    weight_slice = weights[k_base : k_base + tile_k, n_base : n_base + tile_n]
    padded_activations = _pad_matrix(activation_slice.astype(np.int8))
    padded_weights = _pad_matrix(weight_slice.astype(np.int8))

    activation_payload = {
        "layout": "row_major_16xint8_vectors",
        "valid_rows": tile_m,
        "valid_cols": tile_k,
        "rows": padded_activations.astype(int).tolist(),
    }
    weight_payload = {
        "layout": "k_major_rows_of_16_columns",
        "valid_rows": tile_k,
        "valid_cols": tile_n,
        "rows": padded_weights.astype(int).tolist(),
    }

    tile_manifest = {
        "manifest_version": "0.1",
        "package_id": f"{spec.package_id}_tile_{tile_id:03d}",
        "op_type": "gemm_tile_pass",
        "shape": {"m": spec.m, "k": spec.k, "n": spec.n},
        "tile": {
            "m_tile_base": m_base,
            "n_tile_base": n_base,
            "k_tile_base": k_base,
            "tile_rows": tile_m,
            "tile_cols": tile_n,
            "tile_depth": tile_k,
        },
        "execution": {
            "seq_len": tile_k,
            "k_pass_index": tile.k_pass_index,
            "k_tile_count": tile.k_tile_count,
            "acc_clear": tile.acc_clear,
            "emit_drain_after": tile.emit_drain_after,
        },
        "files": {
            "activations": "activations.json",
            "weights": "weights.json",
        },
    }

    write_json(tile_dir / "manifest.json", tile_manifest)
    write_json(tile_dir / "activations.json", activation_payload)
    write_json(tile_dir / "weights.json", weight_payload)

    if tile.emit_drain_after:
        tile_product = product[m_base : m_base + tile_m, n_base : n_base + tile_n]
        golden_payload = {
            "layout": "drain_address_to_16xint32_vector_map",
            "valid_rows": tile_m,
            "valid_cols": tile_n,
            "outputs": _golden_outputs_from_product(tile_product, m_base, n_base),
        }
        tile_manifest["files"]["golden"] = "golden.json"
        write_json(tile_dir / "manifest.json", tile_manifest)
        write_json(tile_dir / "golden.json", golden_payload)

    return {
        "tile_id": tile_id,
        "dir": f"tiles/tile_{tile_id:03d}",
        **asdict(tile),
    }


def export_linear_tiled_package(
    output_dir: str | Path,
    spec: LinearOp | None = None,
    data: LinearData | None = None,
    *,
    tile_order: list[TilePlanEntry] | None = None,
) -> Path:
    spec = spec or LinearOp()
    spec.validate_positive()

    output_path = Path(output_dir)
    output_path.mkdir(parents=True, exist_ok=True)

    activations = _resolve_activation_matrix(spec, data)
    weights = _resolve_weight_matrix(spec, data)
    product = _compute_product(activations, weights, None if data is None else data.bias)
    plan = tile_order if tile_order is not None else plan_linear_tiles(spec)

    tiles_dir = output_path / "tiles"
    tiles_dir.mkdir(exist_ok=True)

    manifest = _build_tiled_root_manifest(spec, plan)

    tile_index: list[dict[str, object]] = []
    for tile_id, tile in enumerate(plan):
        tile_dir = tiles_dir / f"tile_{tile_id:03d}"
        tile_dir.mkdir(exist_ok=True)
        tile_index.append(_emit_tiled_tile(tile_dir, tile_id, spec, tile, activations, weights, product))

    readme = f"""# {spec.package_id}

Generated by `l6_toolchain.export_linear_tiled_package`.

- Logical op: tiled Linear
- Shape: M={spec.m}, K={spec.k}, N={spec.n}
- Tile size: 16x16x16
- Tile count: {len(plan)}

This package is intended for L6 study of tiling, tail handling, and split-K scheduling.
"""

    write_json(output_path / "manifest.json", manifest)
    write_json(output_path / "tiles.json", {"tiles": tile_index})
    (output_path / "README.md").write_text(readme, encoding="utf-8")

    return output_path


def export_linear_tile_package(
    output_dir: str | Path,
    spec: LinearOp | None = None,
    data: LinearData | None = None,
) -> Path:
    spec = spec or LinearOp()
    spec.validate()

    output_path = Path(output_dir)
    output_path.mkdir(parents=True, exist_ok=True)

    activations = _resolve_activation_matrix(spec, data)
    weights = _resolve_weight_matrix(spec, data)
    golden = _golden_outputs_from_product(
        _compute_product(activations, weights, None if data is None else data.bias),
        0,
        0,
    )

    manifest = _build_single_tile_manifest(spec)

    activation_payload = {
        "layout": "row_major_16xint8_vectors",
        "rows": activations.astype(int).tolist(),
    }
    weight_payload = {
        "layout": "k_major_rows_of_16_columns",
        "rows": weights.astype(int).tolist(),
    }
    golden_payload = {
        "layout": "drain_address_to_16xint32_vector_map",
        "outputs": golden,
    }

    readme = f"""# {spec.package_id}

Generated by `l6_toolchain.linear_export`.

- Logical op: single-tile Linear
- Shape: M={spec.m}, K={spec.k}, N={spec.n}
- Activation mode: {spec.activation_mode}
- Weight mode: {spec.weight_mode}

This package is intended for L6 study before moving to arbitrary-shape tiling.
"""

    write_json(output_path / "manifest.json", manifest)
    write_json(output_path / "activations.json", activation_payload)
    write_json(output_path / "weights.json", weight_payload)
    write_json(output_path / "golden.json", golden_payload)
    (output_path / "README.md").write_text(readme, encoding="utf-8")

    return output_path


