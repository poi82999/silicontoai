from .lowering import TILE_SIZE, TilePlanEntry, plan_linear_tiles


def _activation_matrix(spec: LinearOp) -> np.ndarray:
    if spec.activation_mode == "row_ramp":
        base = np.arange(spec.k, dtype=np.int8)
        return np.tile(base, (spec.m, 1))
    if spec.activation_mode == "column_ramp":
        base = np.arange(spec.m, dtype=np.int8).reshape(spec.m, 1)
        return np.tile(base, (1, spec.k))
    if spec.activation_mode == "identity":
        return np.eye(spec.m, spec.k, dtype=np.int8)
    raise ValueError(f"Unsupported activation_mode: {spec.activation_mode}")


def _weight_matrix(spec: LinearOp) -> np.ndarray:
    if spec.weight_mode == "constant_3":
        return np.full((spec.k, spec.n), 3, dtype=np.int8)
    if spec.weight_mode == "identity":
        return np.eye(spec.k, spec.n, dtype=np.int8)
    if spec.weight_mode == "column_ramp":
        base = np.arange(spec.n, dtype=np.int8).reshape(1, spec.n)
        return np.tile(base, (spec.k, 1))
    raise ValueError(f"Unsupported weight_mode: {spec.weight_mode}")


def _resolve_activation_matrix(spec: LinearOp, data: LinearData | None = None) -> np.ndarray:
    if spec.activation_mode in {EXPLICIT_TENSOR_MODE, "from tensor"}:
        if data is None:
            raise ValueError("activation_mode='from_tensor' requires LinearData.activations.")
        if data.activations.shape != (spec.m, spec.k):
            raise ValueError("LinearData.activations must have shape (m, k).")
        return data.activations
    return _activation_matrix(spec)


def _resolve_weight_matrix(spec: LinearOp, data: LinearData | None = None) -> np.ndarray:
    if spec.weight_mode in {EXPLICIT_TENSOR_MODE, "from tensor"}:
        if data is None:
            raise ValueError("weight_mode='from_tensor' requires LinearData.weights.")
        if data.weights.shape != (spec.k, spec.n):
            raise ValueError("LinearData.weights must have shape (k, n).")
        return data.weights
    return _weight_matrix(spec)


def _compute_product(activations: np.ndarray, weights: np.ndarray, bias: np.ndarray | None = None) -> np.ndarray:
    """Bit-accurate FP16→FP32 matmul matching NPU RTL semantics.

    Uses CUDA GPU acceleration when available (via cuda.golden_accel),
    with automatic NumPy fallback.
    """
    try:
        from npu_cuda.golden_accel import compute_golden_single
        return compute_golden_single(activations, weights, bias)
    except ImportError:
        # Cast to float16, multiply/accumulate into float32 to match RTL semantics
        product = activations.astype(np.float16) @ weights.astype(np.float16)
        product = product.astype(np.float32)
        if bias is not None:
            product = product + bias.astype(np.float32)
        return product


def _golden_outputs_from_product(product: np.ndarray, global_row_base: int, global_col_base: int) -> list[dict[str, object]]:
    outputs: list[dict[str, object]] = []
    # Cast float32 matrix directly viewing its IEEE 754 bit layout as int32
    bit_representations = product.view(np.int32).tolist()
    for row_idx, vector in enumerate(bit_representations):
        outputs.append(
            {
                "drain_addr": row_idx,
                "global_row": global_row_base + row_idx,
                "global_col_base": global_col_base,
                "vector": vector,
            }
        )
    return outputs


def _pad_matrix(matrix: np.ndarray, target_rows: int = TILE_SIZE, target_cols: int = TILE_SIZE) -> np.ndarray:
    padded = np.zeros((target_rows, target_cols), dtype=matrix.dtype)
    padded[: matrix.shape[0], : matrix.shape[1]] = matrix
    return padded


def _build_tiled_root_manifest(spec: LinearOp, plan: list[TilePlanEntry]) -> dict[str, object]:
    return {
        "manifest_version": "0.1",
        "package_id": spec.package_id,
        "op_type": "gemm_tiled",
        "description": "L6 study package generated from a tiled Linear export plan.",
        "shape": {"m": spec.m, "k": spec.k, "n": spec.n},
        "tile_size": TILE_SIZE,
        "tile_count": len(plan),
        "files": {"tiles": "tiles.json"},
        "l6_study": {
            "logical_op": "linear",
            "activation_mode": spec.activation_mode,
            "weight_mode": spec.weight_mode,
        },
    }


